import fetch from "node-fetch";

export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).send("Method Not Allowed");
  }

  const TOKEN = process.env.TELEGRAM_BOT_TOKEN;
  const OPENAI_KEY = process.env.OPENAI_API_KEY;

  const body = req.body || {};
  const chatId = body.message?.chat?.id;
  const text = (body.message?.text || "").trim();

  if (!chatId) {
    return res.status(200).send("OK");
  }

  let reply = "ü§ñ";

  if (text) {
    try {
      // üïí –∑–∞—Ç—Ä–∏–º–∫–∞ 1 —Å–µ–∫—É–Ω–¥–∞ ‚Äî —â–æ–± —É–Ω–∏–∫–Ω—É—Ç–∏ –ø–æ–º–∏–ª–∫–∏ 429
      await new Promise(resolve => setTimeout(resolve, 1000));

      // üîπ –∑–∞–ø–∏—Ç –¥–æ OpenAI
      const aiResponse = await fetch("https://api.openai.com/v1/chat/completions", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "Authorization": `Bearer ${OPENAI_KEY}`,
        },
        body: JSON.stringify({
          model: "gpt-4o-mini",
          messages: [{ role: "user", content: text }],
          temperature: 0.7,
        }),
      });

      const data = await aiResponse.json();

      if (data.error) {
        reply = `‚ö†Ô∏è OpenAI error: ${data.error.message}`;
      } else {
        reply = data.choices?.[0]?.message?.content?.trim() || "üò∂ –ù–µ–º–∞—î –≤—ñ–¥–ø–æ–≤—ñ–¥—ñ.";
      }

    } catch (e) {
      console.error("OpenAI error:", e);
      reply = `‚ö†Ô∏è Error connecting to OpenAI: ${e.message}`;
    }
  }

  // üîπ –Ω–∞–¥—Å–∏–ª–∞—î–º–æ –≤—ñ–¥–ø–æ–≤—ñ–¥—å —É Telegram
  await fetch(`https://api.telegram.org/bot${TOKEN}/sendMessage`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      chat_id: chatId,
      text: reply,
    }),
  });

  res.status(200).send("OK");
}